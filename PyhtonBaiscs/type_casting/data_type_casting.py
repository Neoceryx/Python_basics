def casting_examples():

    # methdos for casting int(), float(), list(), tuple(), set()

    val = 5.2;
    print(f"{val}  is  {type(val)}")

    # casting to integer
    val2 = int(val)
    print(f"{val2} is {type(val2)}")

    # casting to sting
    val3 = str(val2)
    print(f"{val3} is  {type(val3)}")

    # casting float
    val4 = float(5)
    print(f"{val4} is {type(val4)}")
